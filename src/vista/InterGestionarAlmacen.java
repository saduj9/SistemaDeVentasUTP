    private void jButton_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_actualizarActionPerformed

        Almacen almacen = new Almacen();
        Ctrl_Almacen controlAlmacen = new Ctrl_Almacen();
        String ubicacion;
        ubicacion = jComboBox_ubicacion.getSelectedItem().toString().trim();

        //Actualizar datos
        almacen.setSku(txt_sku.getText());
        almacen.setStock(Integer.parseInt(txt_stock.getText().trim()));
        if (!ubicacion.equalsIgnoreCase("Seleccione ubicación:")) {
            almacen.setUbicacion(ubicacion);
       
            if (controlAlmacen.actualizar(almacen, idAlmacen)) {
                JOptionPane.showMessageDialog(null, "¡Registro Actualizado!");
                this.CargarTablaAlmacen();
                this.Limpiar();
            } else {
                JOptionPane.showMessageDialog(null, "Error al Actualizar");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione una ubicación para guardar");
        }
    }//GEN-LAST:event_jButton_actualizarActionPerformed


