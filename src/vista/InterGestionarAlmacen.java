    /*
     * *****************************************************
     * metodo para mostrar todos los clientes registrados
     * *****************************************************
     */
    private void CargarTablaAlmacen() {
        Connection con = Conexion.conectar();
        DefaultTableModel model = new DefaultTableModel();
        String sql = "select a.idAlmacen as id, cc.orden_compra as orden,cc.fecha_compra, p.nombre_producto as producto, p.descripcion_prod , "
                + "a.sku ,a.stock ,a.ubicacion  "
                + "from tb_almacen as a, tb_cabecera_compra as cc, tb_producto as p where cc.idCabeceraCompra = a.idCabeceraCompra and p.idProducto = a.idProducto;";
        Statement st;
        try {
            st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            InterGestionarAlmacen.jTable_ventas = new JTable(model);
            InterGestionarAlmacen.jScrollPane1.setViewportView(InterGestionarAlmacen.jTable_ventas);

            model.addColumn("N°");//ID
            model.addColumn("Orden de compra");
            model.addColumn("Fecha de registro");
            model.addColumn("Producto");
            model.addColumn("Descripcion");
            model.addColumn("Sku");
            model.addColumn("Cantidad");
            model.addColumn("ubicacion");
            while (rs.next()) {
                Object fila[] = new Object[8];
                for (int i = 0; i < 8; i++) {
                    
                   fila[i] = rs.getObject(i + 1);                    
                }
                model.addRow(fila);
            }
            con.close();
        } catch (SQLException e) {
            System.out.println("Error al llenar la tabla de almacen: " + e);
        }
        //evento para obtener campo al cual el usuario da click
        //y obtener la interfaz que mostrara la informacion general
        jTable_ventas.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                int fila_point = jTable_ventas.rowAtPoint(e.getPoint());
                int columna_point = 0;

                if (fila_point > -1) {
                    idAlmacen = (int) model.getValueAt(fila_point, columna_point);
                    EnviarDatosProductoSeleccionada(idAlmacen);//metodo
                }
            }
        });
    }


    /*
     * **************************************************
     * Metodo que envia datos seleccionados
     * **************************************************
     */
    private void EnviarDatosProductoSeleccionada(int idAlmacen) {
        try {
            Connection con = Conexion.conectar();
            PreparedStatement pst = con.prepareStatement(
                    "select a.idAlmacen as id, cc.orden_compra as orden,cc.fecha_compra, p.nombre_producto as producto, p.descripcion_prod , "
                + "a.sku ,a.stock ,a.ubicacion  "
                + "from tb_almacen as a, tb_cabecera_compra as cc, tb_producto as p where cc.idCabeceraCompra = a.idCabeceraCompra and p.idProducto = a.idProducto and a.idAlmacen= '"+ idAlmacen +"';");
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                jComboBox_ubicacion.setSelectedItem(rs.getString("ubicacion"));
                txt_sku.setText(rs.getString("sku"));
                txt_stock.setText(String.valueOf(rs.getInt("stock")));
            }
            con.close();
        } catch (SQLException e) {
            System.out.println("Error al seleccionar el id almacen: " + e);
        }
    }

   
   
